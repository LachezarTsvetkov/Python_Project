import unittest

from Game_Logic import Tetris

class TetrisTestCase(unittest.TestCase):

    def setUp(self):
        self.tetris = Tetris(20, 12, 1)

    def test_start_figure(self):
        # Make sure a new figure is created when the function is called
        self.assertIsNone(self.tetris.figure)
        self.tetris.newFigure()
        self.assertIsNotNone(self.tetris.figure)


    def test_new_figure(self):
        self.assertIsNone(self.tetris.figure)
        self.tetris.newFigure()

        oldFigure = self.tetris.figure.type
        self.tetris.newFigure()

        self.assertNotEqual(oldFigure, self.tetris.figure.type)

    def test_collides(self):
        # Test that the function returns True when a collision occurs
        self.tetris.newFigure()
        self.tetris.figure.x = 0
        self.tetris.figure.y = 170
        self.assertTrue(self.tetris.collides())

    def test_freeze(self):
        # Test that the function correctly freezes a figure
        self.tetris.newFigure()
        self.tetris.figure.x = 4
        self.tetris.figure.y = 17
        self.tetris.freeze()
        for i in range(4):
            for j in range(4):
                if i * 4 + j in self.tetris.figure.image():
                    self.assertNotEqual(self.tetris.field[i + self.tetris.figure.y][j + self.tetris.figure.x], 0)


